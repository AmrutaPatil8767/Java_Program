class Switch1
{
	public static void main(String args[])
	{
		int Course = 4;
		switch(Course)
		{
			case 1:
				System.out.println("BCS");
				break;
			case 2:
				System.out.println("BCA");
				break;
			case 3:
				System.out.println("MCS");
				break;
			case 4:
				System.out.println("MCA");
				break;
			default :
				System.out.println("Wrong Course!!");
		}
	}
}
			